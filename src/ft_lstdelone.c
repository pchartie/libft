#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	t_list *lst;

	if (alst && *alst)
	{
		lst = *alst;
		*alst = lst->next;
		del(lst->content, lst->content_size);
		free(lst);
		*alst = NULL;
	}
}
