#include "libft.h"

int		*ft_tabnew(size_t size)
{
	int *tab;

	tab = (int *)malloc(sizeof(int) * size);
	if (!tab)
		return (NULL);
	size--;
	while (size)
		tab[size--] = 0;
	return (tab);
}
