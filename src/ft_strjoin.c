#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;
	int		s1taille;
	int		s2taille;
	int		i;

	i = 0;
	s1taille = ft_strlen(s1);
	s2taille = ft_strlen(s2);
	res = (char *)malloc(sizeof(char) * (s1taille + s2taille) + 1);
	if (!res)
		return (NULL);
	while (*s1)
		res[i++] = *s1++;
	while (*s2)
		res[i++] = *s2++;
	res[i] = '\0';
	return (res);
}
