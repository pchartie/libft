#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	char	*d;
	size_t	i;

	d = (char *)dst;
	i = 0;
	while (*src && i < n)
	{
		*d++ = *src++;
		i++;
	}
	while (i < n)
	{
		*d++ = '\0';
		i++;
	}
	return (dst);
}
