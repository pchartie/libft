#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *lst;
	t_list *current;

	lst = *alst;
	while (lst)
	{
		current = lst;
		lst = lst->next;
		del(current->content, current->content_size);
		free(current);
	}
	*alst = NULL;
}
